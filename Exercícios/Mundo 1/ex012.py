# Ângulos sen, cos, tang
import math
angulo = float(input('Digite o ângulo: '))
cos = math.cos(math.radians(angulo))
sen = math.sin(math.radians(angulo))
tang = math.tan(math.radians(angulo))
print('O cosseno do ângulo é: {}'.format(cos))
print('O seno do ângulo é: {}'.format(sen))
print('A tangente do ângulo é: {}'.format(tang))


